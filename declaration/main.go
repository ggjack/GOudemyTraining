package main

import "fmt"

func main() {
	// shorthand (declare, assign)
	a := 10
	b := "golang"
	c := 4.16
	d := true
	fmt.Printf("%v \t %v \t %v \t %v \n", a, b, c, d)

	// declaration (declaration)

	var e int
	var f string
	var g float64
	var h bool
	fmt.Printf("%v \t %v \t %v \t %v \n", e, f, g, h)

}
